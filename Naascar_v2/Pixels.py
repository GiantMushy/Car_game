    def display_text(self, letter_posx, letter_posy, text, pixel_size):
        pixel = [[0,0],[pixel_size,0],[pixel_size,pixel_size],[0,pixel_size]]
        letter_count = 0
        glColor3f(1.0, 1.0, 1.0)
        for char in text:
            if char in self.letter_dict:
                for pixel_posx in range(8):
                    for pixel_posy in self.letter_dict[char][pixel_posx]:
                        x = letter_posx + pixel_posx * pixel_size + letter_count
                        y = letter_posy + pixel_posy * pixel_size
                        glBegin(GL_TRIANGLE_FAN)
                        for c in range(4):
                            glVertex2f(x + pixel[c][0], y + pixel[c][1])
                        glEnd()
            letter_count += 9 * pixel_size